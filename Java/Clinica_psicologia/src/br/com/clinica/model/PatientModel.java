package br.com.clinica.model;

public class PatientModel {
	private String cpf;
	private String nome;
	private String rua;
	private String cidade;
	private String telefone;
	private String medicamento1;
	private String medicamento2;
	private String dosagem1;
	private String dosagem2;
	
	public String getCpf() {
		return cpf;
	}
	
	public void setCpf(String cpf) {
		this.cpf = cpf;
	}
	
	public String getNome() {
		return nome;
	}
	
	public void setNome(String nome) {
		this.nome = nome;
	}
	
	public String getRua() {
		return rua;
	}
	
	public void setRua(String rua) {
		this.rua = rua;
	}
	
	public String getCidade() {
		return cidade;
	}
	
	public void setCidade(String cidade) {
		this.cidade = cidade;
	}
	
	public String getTelefone() {
		return telefone;
	}
	
	public void setTelefone(String telefone) {
		this.telefone = telefone;
	}

	public String getMedicamento1() {
		return medicamento1;
	}

	public void setMedicamento1(String medicamento1) {
		this.medicamento1 = medicamento1;
	}

	public String getMedicamento2() {
		return medicamento2;
	}

	public void setMedicamento2(String medicamento2) {
		this.medicamento2 = medicamento2;
	}

	public String getDosagem1() {
		return dosagem1;
	}

	public void setDosagem1(String dosagem1) {
		this.dosagem1 = dosagem1;
	}

	public String getDosagem2() {
		return dosagem2;
	}

	public void setDosagem2(String dosagem2) {
		this.dosagem2 = dosagem2;
	}
	
	
	
	
}
